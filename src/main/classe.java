package main;

public class classe {
	
	
	
	public classe() {}
	private String mere;
	private String nom;
	private String lim;
	private String mens;
	private String insc;
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getLim() {
		return lim;
	}
	public void setLim(String lim) {
		this.lim = lim;
	}
	public String getMens() {
		return mens;
	}
	public void setMens(String mens) {
		this.mens = mens;
	}
	public String getInsc() {
		return insc;
	}
	public void setInsc(String insc) {
		this.insc = insc;
	}
	public String getMere() {
		return mere;
	}
	public void setMere(String mere) {
		this.mere = mere;
	}
	

}
